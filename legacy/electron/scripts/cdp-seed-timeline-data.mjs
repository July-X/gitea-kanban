#!/usr/bin/env node
/**
 * 给 Gitea demo 仓库注入时间轴测试数据。
 *
 * 用法：
 *   node scripts/cdp-seed-timeline-data.mjs [分支名前缀] [commit 数量]
 *
 *   默认：分支名 prefix = timeline-test，commit 数 = 30
 *
 * 前提：
 *   - 本地有 Gitea 实例跑在 `localhost:3000`（任何来源的本地实例都可）
 *   - 已给 kanban_demo 生成 access token（见下面 TOKEN 获取方式）
 */
import http from 'node:http';
import https from 'node:https';

const GITEA_URL = process.env.GITEA_URL ?? 'http://localhost:3000';
const OWNER = 'kanban_demo';
const REPO = 'm4java-test';
const TOKEN = process.env.GITEA_TOKEN ?? '98997cbd9d76532e4a7d9c77761d407d86a506df';

const branchPrefix = process.argv[2] ?? 'timeline-test';
const commitCount = Number(process.argv[3] ?? 30);
const newBranch = `${branchPrefix}-${Date.now()}`;

function fetchJson(path, method = 'GET', body = null) {
  return new Promise((resolve, reject) => {
    const url = new URL(path, GITEA_URL);
    const mod = url.protocol === 'https:' ? https : http;
    const req = mod.request(
      url,
      {
        method,
        headers: {
          Authorization: `token ${TOKEN}`,
          'Content-Type': 'application/json',
          Accept: 'application/json',
        },
      },
      (res) => {
        let data = '';
        res.on('data', (c) => (data += c));
        res.on('end', () => {
          try {
            const parsed = data ? JSON.parse(data) : {};
            if (res.statusCode >= 200 && res.statusCode < 300) {
              resolve(parsed);
            } else {
              reject(new Error(`HTTP ${res.statusCode}: ${data.slice(0, 200)}`));
            }
          } catch (e) {
            reject(new Error(`parse fail: ${e.message}\n${data.slice(0, 200)}`));
          }
        });
      },
    );
    req.on('error', reject);
    if (body) req.write(JSON.stringify(body));
    req.end();
  });
}

async function main() {
  console.log(`Seeding ${OWNER}/${REPO}: branch=${newBranch}, commits=${commitCount}`);

  // 1. 拿默认分支信息
  const defaultBranchInfo = await fetchJson(`/api/v1/repos/${OWNER}/${REPO}/branches/main`);
  const baseSha = defaultBranchInfo.commit?.id;
  if (!baseSha) throw new Error('cannot get main branch commit id');
  console.log('base commit:', baseSha);

  // 2. 创建新分支
  await fetchJson(`/api/v1/repos/${OWNER}/${REPO}/branches`, 'POST', {
    new_branch_name: newBranch,
    old_branch_name: 'main',
  });
  console.log('created branch:', newBranch);

  // 3. 在该分支上创建多个文件 commit
  let parentSha = baseSha;
  for (let i = 0; i < commitCount; i++) {
    const path = `timeline-data/${newBranch}/file-${i}.md`;
    const content = Buffer.from(`# commit ${i} on ${newBranch}\n\ngenerated by cdp-seed-timeline-data.mjs`).toString('base64');
    const r = await fetchJson(`/api/v1/repos/${OWNER}/${REPO}/contents/${encodeURIComponent(path)}`, 'POST', {
      branch: newBranch,
      content,
      message: `test: add ${newBranch} file ${i}`,
      author: { email: 'cdp-test@example.com', name: 'CDP Test Bot' },
    });
    parentSha = r.commit?.id ?? parentSha;
    if ((i + 1) % 10 === 0) console.log(`  progress: ${i + 1}/${commitCount}`);
  }

  console.log('\nDone.');
  console.log(`  branch: ${newBranch}`);
  console.log(`  commits: ${commitCount}`);
  console.log(`  url: ${GITEA_URL}/${OWNER}/${REPO}/src/branch/${encodeURIComponent(newBranch)}`);
  console.log('\nTo test in gitea-kanban:');
  console.log('  1. pnpm dev');
  console.log('  2. 打开时间轴，等待分支列表刷新');
  console.log(`  3. 选择 "${newBranch}" 查看 ${commitCount} 条 commit`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
